#!/bin/sh

while read line 
do
echo "${line}" | sed s/\\\\/\\\\\\\\/g\;\
\	s/\|/\\\\\\\|/g\;\
\	s/\*/\\\\\\\*/g\;\
\	s/\?/\\\\\\\?/g\;\
\	s/\\[/\\\\\[/g\;\
\	s/\\]/\\\\\]/g\;\
\	s/\'/\\\\\'/g\;\
\	s/\"/\\\\\"/g\;\
\	s/\\\;/\\\\\;/g\;\
\	s/\\$/\\\\$/g\;\
\	s/\\\&/\\\\\&/g\;\
\	s/\(/\\\\\(/g\;\
\	s/\)/\\\\\)/g\;\
\	s/\\^/\\\\\^/g\;\
\	s/\</\\\\\</g\;\
\	s/\>/\\\\\>/g\;\
\	s/\\\s/\\\\\ /g\;\
\	s/\\\t/\\\\\\t/g
done < /dev/stdin
